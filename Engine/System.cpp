#pragma once

#include "System.hpp"

//----------------------------------------------------------------------------//
// System
//----------------------------------------------------------------------------//

System* System::s_first = nullptr;
System* System::s_last = nullptr;

//----------------------------------------------------------------------------//
System::System(void)
{
	Link(s_first, s_last, this, m_prev);
}
//----------------------------------------------------------------------------//
System::~System(void)
{
	Unlink(s_first, s_last, this, m_prev);
}
//----------------------------------------------------------------------------//
EventResult System::SendEvent(int _type, void* _data)
{
	EventResult _r = ER_Pass;
	for (System* i = s_first; i; i = i->m_next)
	{
		if ((_r = i->_OnEvent(_type, _data)) != ER_Pass)
			break;
	}
	return _r;
}
