#pragma once

#include "Image.hpp"

//----------------------------------------------------------------------------//
// Image
//----------------------------------------------------------------------------//

Image* Image::s_images = nullptr;

//----------------------------------------------------------------------------//
Image::Image(uint _name) :
	m_name(_name)
{
	Link(s_images, this, m_prev);
}
//----------------------------------------------------------------------------//
Image::~Image(void)
{
	Unlink(s_images, this, m_prev);
}
//----------------------------------------------------------------------------//
Image* Image::GetAsset(uint _name)
{
	for (Image* i = s_images; i; i = i->m_next)
	{
		if (i->m_name == _name)
			return i;
	}
	return nullptr;
}
